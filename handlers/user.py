        [InlineKeyboardButton(text="üé´ 1 –±–∏–ª–µ—Ç", callback_data="ticket_single")],
        [InlineKeyboardButton(text="üéü –£ –º–µ–Ω—è –µ—Å—Ç—å –ø—Ä–æ–º–æ–∫–æ–¥", callback_data="ticket_promocode")]
